-- SEED EMPRESAS AND ADD UNIQUE CONSTRAINT
-- ============================================

-- 1. Add Unique Constraint to CNPJ (cleaning duplicates first if any)
-- (Using a safe way to keep the newest version if duplicates exist)
DELETE FROM public.empresas a USING (
    SELECT id, ROW_NUMBER() OVER (PARTITION BY cnpj ORDER BY created_at DESC) as row_num
    FROM public.empresas
    WHERE cnpj IS NOT NULL AND cnpj <> ''
) b
WHERE a.id = b.id AND b.row_num > 1;

DO $$ 
BEGIN 
    IF NOT EXISTS (SELECT 1 FROM pg_constraint WHERE conname = 'unique_cnpj') THEN
        ALTER TABLE public.empresas ADD CONSTRAINT unique_cnpj UNIQUE (cnpj);
    END IF;
END $$;

